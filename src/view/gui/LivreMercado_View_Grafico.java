    private String mostraInfoProduto(Produto produto, String campo){
        if(campo.equalsIgnoreCase("nome")){
            return produto.getNome();
        } else if(campo.equalsIgnoreCase("descricao") || campo.equalsIgnoreCase("descrição") ){
            return produto.getDescricao();
        } else if(campo.equalsIgnoreCase("preco")){
            return  ""+produto.getPrecoBase();
        }else{
    return null;
}
}
    
