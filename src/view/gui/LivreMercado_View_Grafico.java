    private void initTree(LivreMercado model){
        JTree tree = this.arvoreCategorias;
        
        DefaultMutableTreeNode root = new DefaultMutableTreeNode("Categorias");
        TreeModel treeModel = new DefaultTreeModel(root);
        
        this.passAllSub(model.getCategoriaRaiz(), root);
        
        joshModel = treeModel;
        
        
    }
    
    private void passAllSub(Categoria_if categoriaPai, DefaultMutableTreeNode nodeAnterior){
        DefaultMutableTreeNode newNode = new DefaultMutableTreeNode(categoriaPai.getNome());
        nodeAnterior.add(newNode);
        System.out.println(categoriaPai.getNome());
        if(!categoriaPai.getSubcategorias().isEmpty()){ //se categoria de olho tenha filhos
            for(Categoria_if categoriaFilha: categoriaPai.getSubcategorias()){
                passAllSub(categoriaFilha, newNode);
            }
        } else{ //caso ela não tenha então ela é a ultima subcategoria
            if(!categoriaPai.getProdutos().isEmpty()){ //caso essa ultima categoria tenha algum produto
                for(Produto produto: categoriaPai.getProdutos()){
                    DefaultMutableTreeNode newNewNode = new DefaultMutableTreeNode(produto.getNome());
                    newNode.add(newNewNode);
                    nodeProductMap.put(newNewNode, produto);
                }
                
            }
        }
    }
   
    

